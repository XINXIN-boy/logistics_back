package com.itqf.entity;


public class Callbackinfo {

  private int id;
  private String billId;
  private String billType;
  private String content;
  private String dialNo;
  private java.sql.Date finallyDialTime;
  private String goodsBillId;
  private String locked;
  private String success;
  private String type;
  private java.sql.Date writeTime;
  private String writer;


  public int getId() {
    return id;
  }

  public void setId(int id) {
    this.id = id;
  }


  public String getBillId() {
    return billId;
  }

  public void setBillId(String billId) {
    this.billId = billId;
  }


  public String getBillType() {
    return billType;
  }

  public void setBillType(String billType) {
    this.billType = billType;
  }


  public String getContent() {
    return content;
  }

  public void setContent(String content) {
    this.content = content;
  }


  public String getDialNo() {
    return dialNo;
  }

  public void setDialNo(String dialNo) {
    this.dialNo = dialNo;
  }


  public java.sql.Date getFinallyDialTime() {
    return finallyDialTime;
  }

  public void setFinallyDialTime(java.sql.Date finallyDialTime) {
    this.finallyDialTime = finallyDialTime;
  }


  public String getGoodsBillId() {
    return goodsBillId;
  }

  public void setGoodsBillId(String goodsBillId) {
    this.goodsBillId = goodsBillId;
  }


  public String getLocked() {
    return locked;
  }

  public void setLocked(String locked) {
    this.locked = locked;
  }


  public String getSuccess() {
    return success;
  }

  public void setSuccess(String success) {
    this.success = success;
  }


  public String getType() {
    return type;
  }

  public void setType(String type) {
    this.type = type;
  }


  public java.sql.Date getWriteTime() {
    return writeTime;
  }

  public void setWriteTime(java.sql.Date writeTime) {
    this.writeTime = writeTime;
  }


  public String getWriter() {
    return writer;
  }

  public void setWriter(String writer) {
    this.writer = writer;
  }

}
